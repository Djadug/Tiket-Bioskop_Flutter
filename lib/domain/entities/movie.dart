class Movie {
  final String id;
  final String title;
  final String poster;
  final String synopsis;
  final int duration;
  final double rating;
  final List<String> genres;

  Movie({
    required this.id,
    required this.title,
    required this.poster,
    required this.synopsis,
    required this.duration,
    required this.rating,
    required this.genres,
  });
}


